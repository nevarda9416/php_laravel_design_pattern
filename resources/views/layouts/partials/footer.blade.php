    function getArticleList(catId, holder) {
        var url = 'https://www.phucanh.vn/ajax/get_json.php?action=article&action_type=list&type=article&catId='+catId+'&show=10';

        $.getJSON(url, function(result) {
            var data = result.list;
            var html = "";
            Object.keys(data).forEach(function(key, keyIndex) {
                if (keyIndex < 5) {
                    var url         = data[key].url;
                    var image       = data[key].image.thum;
                    var title       = data[key].title;
                    var lastUpdate  = data[key].lastUpdate;

                    html+=`
                        <li>
                            <a href="`+url+`">
                                <img src="`+image+`" alt="`+title+`" />
                                <span class="a-right">
                                    <h4 class="name h-title">`+title+`</h4>
                                    <span class="time">`+lastUpdate+`</span>
                                </span>
                            </a>
                        </li>
                    `;
                }
            });

            $(holder).html(html);
        });
    }
